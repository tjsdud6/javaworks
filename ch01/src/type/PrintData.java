package type;

public class PrintData {

	public static void main(String[] args) {
		//숫자
		System.out.println(100);
		System.out.println(3.3);
		System.out.println(100+10); //숫자 더하기
		
		System.out.println("====================");
		
		//문자
		System.out.println("A"); //홑따 - 문자표기
		System.out.println("B");  //쌍따 - 문자열 표기 
		System.out.println("banana"); //쌍따 - 문자열 표기
		System.out.println(100 + "10"); //문자 연결
		
		System.out.println("====================");
		
		//불리언
		System.out.println(true);
		System.out.println(4 > 5);
	}
	

}
